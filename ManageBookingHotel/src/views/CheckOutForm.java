    private void jButton_add_serviceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_add_serviceActionPerformed
        int a = JOptionPane.showConfirmDialog(null, "Bạn có muốn sử dụng dịch vụ đã chọn?", "Chọn", JOptionPane.YES_NO_OPTION);
        if(a==0){
            if(jLabel_name_client.getText().equals("")){
                JOptionPane.showMessageDialog(rootPane, "Chọn khách hàng sử dụng dịch vụ!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            }
            else{
                int room_number = Integer.parseInt(jLabel_room_number.getText());
                String service_name = (String) jComboBox_service.getSelectedItem();
                float price_unit = 0; 
                ServiceController service = new ServiceController();
                List<Service> listType = service.loadPrice(service_name);
                for(Service item: listType)
                {
                    price_unit = item.getPrice_unit();
                }
                int amount = (Integer) jSpinner_amount.getValue();
                float price_total = price_unit*amount;
                Date date_buy = new Date();
                ServiceDetail detail = new ServiceDetail();
                detail.setRoom_number(room_number);
                detail.setService_name(service_name);
                detail.setPrice_unit(price_unit);
                detail.setAmount(amount);
                detail.setPrice_total(price_total);
                detail.setDate_buy(date_buy);
                try 
                {
                    ServiceDetailController.add(detail);
                    JOptionPane.showMessageDialog(rootPane, "Thêm dịch thành công!", "Thành công", JOptionPane.INFORMATION_MESSAGE);

                } catch (Exception e) {
                    JOptionPane.showMessageDialog(rootPane, "Thêm thất bại!", "Lỗi", JOptionPane.WARNING_MESSAGE);
                }
            }
        }
        
    }//GEN-LAST:event_jButton_add_serviceActionPerformed

