    public void showTable(){
        BookingDetailsController bookingdetails = new BookingDetailsController();
        jTable_check_out.setModel(new DefaultTableModel(null, new Object[]{"Tên khách", "Số điện thoại", "Mail", "Số phòng", "Giá", "Ngày nhận phòng"}));
        DefaultTableModel tableModel = (DefaultTableModel) jTable_check_out.getModel();
        List<BookingDetails> list = bookingdetails.allBookingDetail();

        for (int i = 0; i < list.size(); i++) {

            BookingDetails bd = (BookingDetails)list.get(i);
            Vector row = new Vector();
            row.add(bd.getFullname_client());
            row.add(bd.getPhone_number());
            row.add(bd.getMail());
            row.add(bd.getRoom_number());
            row.add(bd.getPrice());
            row.add(bd.getDate_in());
            tableModel.addRow(row);
        }
    }

    
    
    
    
    public void showService(){
        ServiceController service = new ServiceController();
        List<Service> listType = service.allService();
        jComboBox_service.removeAllItems();
        for(Service item: listType)
        {
           jComboBox_service.addItem(item.getService_name());
        }
    } 
    
    public void showPrice(){
        String service_name = (String) jComboBox_service.getSelectedItem();
        int amount = (Integer) jSpinner_amount.getValue();
        ServiceController service = new ServiceController();
        List<Service> listType = service.loadPrice(service_name);
        for(Service item: listType)
        {
            jLabel_price_unit.setText(String.valueOf(item.getPrice_unit()*amount));
        }
    }
    
    
    
    
    
    
