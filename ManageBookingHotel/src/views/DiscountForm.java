    private void jButton_deleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton_deleteMouseClicked
        DefaultTableModel model = (DefaultTableModel)jTable_discount.getModel();
        int rowIndex = jTable_discount.getSelectedRow();
        int id = Integer.parseInt(model.getValueAt(rowIndex,0).toString());

        if(rowIndex >= 0){

            int a = JOptionPane.showConfirmDialog(rootPane, "Bạn có chắc muốn xóa?", "Xóa", JOptionPane.YES_NO_OPTION);
            if(a==0){
                DiscountController.delete(id);
                showTable();
                jTextField_discount_code.setText("");
                jTextField_discount_price.setText("");
                jDateChooser_date_begin.setDate(null);
                jDateChooser_date_end.setDate(null);
                JOptionPane.showMessageDialog(rootPane, "Xóa thành công!", "Thành công", JOptionPane.INFORMATION_MESSAGE);
            }
        }
        else{
            JOptionPane.showMessageDialog(rootPane, "Vui lòng chọn phòng cần xóa!", "Lỗi", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton_deleteMouseClicked

    private void jButton_updateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton_updateMouseClicked

        DefaultTableModel model = (DefaultTableModel)jTable_discount.getModel();
        int rowIndex = jTable_discount.getSelectedRow();
        int id = Integer.parseInt(model.getValueAt(rowIndex,0).toString());
        String discount_code = jTextField_discount_code.getText();
        float discount_price = Float.parseFloat(jTextField_discount_price.getText());
        Date date_begin = jDateChooser_date_begin.getDate();
        Date date_end = jDateChooser_date_end.getDate();

        if(rowIndex >= 0){
            try {
                Discount discount = new Discount(id, discount_code, discount_price, date_begin, date_end);
                DiscountController.update(discount);
                showTable();
                jTextField_discount_code.setText("");
                jTextField_discount_price.setText("");
                jDateChooser_date_begin.setDate(null);
                jDateChooser_date_end.setDate(null);
                JOptionPane.showMessageDialog(rootPane, "Cập nhật thành công!", "Thành công", JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Cập nhật thất bại!", "Lỗi", JOptionPane.WARNING_MESSAGE);
            }

        }else{
            JOptionPane.showMessageDialog(rootPane, "Vui lòng chọn loại cần cập nhật!", "Lỗi", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton_updateMouseClicked

    private void jButton_addMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton_addMouseClicked

        String discount_code = jTextField_discount_code.getText();
        float discount_price = Float.parseFloat(jTextField_discount_price.getText());
        Date date_begin = jDateChooser_date_begin.getDate();
        Date date_end = jDateChooser_date_end.getDate();

        //Kiểm tra điền đủ thông tin hay chưa?
        if(jTextField_discount_code.getText().equals("")||jTextField_discount_price.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Vui lòng điền đủ thông tin!", "Lỗi", JOptionPane.WARNING_MESSAGE);
        }
        else{
            try {
                Discount discount = new Discount();
                discount.setDiscount_code(discount_code);
                discount.setDiscount_price(discount_price);
                discount.setDate_begin(date_begin);
                discount.setDate_end(date_end);
                DiscountController.add(discount);
                showTable();
                jTextField_discount_code.setText("");
                jTextField_discount_price.setText("");
                jDateChooser_date_begin.setDate(null);
                jDateChooser_date_end.setDate(null);
                JOptionPane.showMessageDialog(rootPane, "Thêm thành công!", "Thành công", JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Thêm thất bại!", "Lỗi", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton_addMouseClicked

    private void jTable_discountMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_discountMouseClicked

        DefaultTableModel model = (DefaultTableModel)jTable_discount.getModel();
        int rowIndex = jTable_discount.getSelectedRow();
        jTextField_discount_code.setText(model.getValueAt(rowIndex,1).toString());
        jTextField_discount_price.setText(model.getValueAt(rowIndex,2).toString());
        
        try {
            Date date_begin = new SimpleDateFormat("yyyy-MM-dd").parse(model.getValueAt(rowIndex,3).toString());
            jDateChooser_date_begin.setDate(date_begin);
            Date date_end = new SimpleDateFormat("yyyy-MM-dd").parse(model.getValueAt(rowIndex,3).toString());
            jDateChooser_date_end.setDate(date_end);
        } catch (ParseException ex) {
            Logger.getLogger(DiscountForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jTable_discountMouseClicked

