    public void showTable(){
        DiscountController discountC = new DiscountController();
        jTable_discount.setModel(new DefaultTableModel(null, new Object[]{"Id", "Mã giảm giá", "Giảm giá %", "Ngày bắt đầu", "Ngày kết thúc"}));
        DefaultTableModel tableModel = (DefaultTableModel) jTable_discount.getModel();
        List<Discount> list = discountC.allDiscount();
        
        for (int i = 0; i < list.size(); i++) {
            Discount discount = (Discount)list.get(i);
            Vector row = new Vector();
            row.add(discount.getId());
            row.add(discount.getDiscount_code());
            row.add(discount.getDiscount_price());
            row.add(discount.getDate_begin());
            row.add(discount.getDate_end());
            tableModel.addRow(row);
        }
    }
    
    
    
    
    
    
