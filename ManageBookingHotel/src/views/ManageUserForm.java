    public void showTable(){
        UserController roomC = new UserController();
        jTable_user.setModel(new DefaultTableModel(null, new Object[]{"Họ tên", "Tên đăng nhập", "Mật khẩu", "Ngày sinh", "Số điện thoại", "Mail", "Địa chỉ", "Trạng thái"}));
        DefaultTableModel tableModel = (DefaultTableModel) jTable_user.getModel();
        List<User> list = roomC.allUser();
        
        for (int i = 0; i < list.size(); i++) {
            User user = (User)list.get(i);
            Vector row = new Vector();
            row.add(user.getFullname());
            row.add(user.getUsername());
            row.add(user.getPassword());
            row.add(user.getBirth_day());
            row.add(user.getPhonenumber());
            row.add(user.getMail());
            row.add(user.getAddress());
            row.add(user.getStatus());
            tableModel.addRow(row);
        }
    }
    
    
    
    
    private void jTable_userMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_userMouseClicked

        DefaultTableModel model = (DefaultTableModel)jTable_user.getModel();
        int rowIndex = jTable_user.getSelectedRow();

        jTextField_username.setText(model.getValueAt(rowIndex,1).toString());
        jTextField_password.setText(model.getValueAt(rowIndex,2).toString());
        jTextField_phone.setText(model.getValueAt(rowIndex,4).toString());
        jTextField_mail.setText(model.getValueAt(rowIndex,5).toString());
        jTextArea_address.setText(model.getValueAt(rowIndex,6).toString());

        if("Open".equals(model.getValueAt(rowIndex,7).toString())){
            jRadioButton_open.setSelected(true);
        }
        else if("Block".equals(model.getValueAt(rowIndex,7).toString())){
            jRadioButton_block.setSelected(true);
        }
        else{
            jRadioButton_admin.setSelected(true);
        }
        getId();
    }//GEN-LAST:event_jTable_userMouseClicked

    
    
    public void getId(){
        String username = jTextField_username.getText();
        UserController userC = new UserController();
        List<User> list = userC.findUsername(username); 
        if(list.size() >0){
            for(User item:list)
            {                       
                id = item.getId();
            }
        }
    }
    
    
    
    
    
    
    private void jButton_deleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton_deleteMouseClicked

        int rowIndex = jTable_user.getSelectedRow();

        if(rowIndex >= 0){
            User user = new User();
            user.setId(id);
            int a = JOptionPane.showConfirmDialog(rootPane, "Bạn có chắc muốn xóa?", "Xóa", JOptionPane.YES_NO_OPTION);
            if(a==0){
                UserController.delete(user);
                showTable();
                JOptionPane.showMessageDialog(rootPane, "Xóa người dùng thành công!", "Thành công", JOptionPane.INFORMATION_MESSAGE);
            }
        }
        else{
            JOptionPane.showMessageDialog(rootPane, "Vui lòng chọn người dùng cần xóa cần xóa!", "Lỗi", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton_deleteMouseClicked

