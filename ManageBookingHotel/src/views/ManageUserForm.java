    private void jButton_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_searchActionPerformed
        String fullname = jTextField_search.getText();
        String username = jTextField_search.getText();
        if(jTextField_search.getText().equals("")){
            showTable();
        }else{
            UserController userC = new UserController();
            jTable_user.setModel(new DefaultTableModel(null, new Object[]{"Họ tên", "Tên đăng nhập", "Mật khẩu", "Ngày sinh", "Số điện thoại", "Mail", "Địa chỉ", "Trạng thái"}));
            DefaultTableModel tableModel = (DefaultTableModel) jTable_user.getModel();
            List<User> list = userC.search(fullname, username);

            if(list.size() == 0){
                JOptionPane.showMessageDialog(rootPane, "Không tìm thấy!", "Tìm kiếm", JOptionPane.OK_OPTION);
                showTable();
                jTextField_search.setText("");
            }else{
                for (int i = 0; i < list.size(); i++) {
                    User user = (User)list.get(i);
                    Vector row = new Vector();
                    row.add(user.getFullname());
                    row.add(user.getUsername());
                    row.add(user.getPassword());
                    row.add(user.getBirth_day());
                    row.add(user.getPhonenumber());
                    row.add(user.getMail());
                    row.add(user.getAddress());
                    row.add(user.getStatus());
                    
                    tableModel.addRow(row);
                }
                jTextField_search.setText("");
            }
        }

    }//GEN-LAST:event_jButton_searchActionPerformed

